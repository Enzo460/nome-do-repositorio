# calculadora.py
def main():
    # Solicita ao usuário dois números
    num1 = float(input("Digite o primeiro número: "))
    num2 = float(input("Digite o segundo número: "))

    # Calcula a soma
    soma = num1 + num2

    # Exibe o resultado
    print(f"A soma de {num1} e {num2} é {soma}")

# Executa o programa
if __name__ == "__main__":
    main()
